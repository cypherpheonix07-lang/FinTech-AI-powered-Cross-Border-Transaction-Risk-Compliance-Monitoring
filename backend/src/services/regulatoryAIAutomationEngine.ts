export type RegulationType = 'AML' | 'KYC' | 'BASEL_IV' | 'MiFID_II' | 'PSD3' | 'GDPR' | 'CCPA' | 'SOX' | 'MICA' | 'DORA' | 'FATF' | 'IFRS_9' | 'CCAR' | 'STRESS_TEST' | 'FBAR' | 'CTR' | 'SAR';
export type FilingStatus = 'DRAFT' | 'PENDING_REVIEW' | 'SUBMITTED' | 'ACCEPTED' | 'REJECTED' | 'AMENDED' | 'WITHDRAWN';
export type JurisdictionCode = 'US' | 'EU' | 'UK' | 'SG' | 'HK' | 'JP' | 'AU' | 'CA' | 'IN' | 'BR' | 'ZA' | 'AE' | 'CH' | 'NG' | 'KR' | 'MX';
export type RegulatoryBody = 'FinCEN' | 'SEC' | 'OCC' | 'FRB' | 'FDIC' | 'CFPB' | 'ECB' | 'EBA' | 'ESMA' | 'FCA' | 'MAS' | 'SFC' | 'FSA_JP' | 'APRA' | 'OSFI' | 'SEBI' | 'BCB' | 'SARB' | 'CBUAE' | 'FINMA' | 'CBN' | 'FSC_KR';
export type ChangeType = 'NEW_REGULATION' | 'AMENDMENT' | 'GUIDANCE' | 'ENFORCEMENT_ACTION' | 'CONSULTATION_PAPER' | 'FINAL_RULE' | 'INTERIM_RULE' | 'NO_ACTION_LETTER';

// ---- REGULATION TYPES ----------------------------------------------------

export interface RegulatoryDocument {
  docId: string;
  title: string;
  regulationType: RegulationType;
  jurisdiction: JurisdictionCode;
  regulatoryBody: RegulatoryBody;
  changeType: ChangeType;
  publicationDate: string;
  effectiveDate: string;
  deadlineDate?: string;
  summary: string;
  fullTextUrl?: string;
  extractedRequirements: RegulatoryRequirement[];
  impactAssessment?: ImpactAssessment;
  parsedAt: string;
  nlpConfidence: number;       // 0-1: how confident NLP extraction is
}

export interface RegulatoryRequirement {
  requirementId: string;
  category: string;           // e.g. 'REPORTING' | 'DATA_RETENTION' | 'CAPITAL' | 'LIQUIDITY'
  description: string;
  obligation: 'MANDATORY' | 'CONDITIONAL' | 'RECOMMENDED' | 'PROHIBITED';
  applicableTo: string[];     // Entity types: 'BANK' | 'PAYMENT_INSTITUTION' | 'CRYPTO_ASSET_SERVICE_PROVIDER'
  threshold?: { metric: string; value: number; unit: string };
  penalty?: { maxAmountUSD: number; description: string; criminal: boolean };
  deadline?: string;
  crossReferences: string[];
}

export interface ImpactAssessment {
  assessmentId: string;
  docId: string;
  operationalImpact: 'LOW' | 'MEDIUM' | 'HIGH' | 'CRITICAL';
  technicalChangesRequired: string[];
  estimatedComplianceCostUSD: number;
  affectedBusinessLines: string[];
  affectedSystems: string[];
  gapAnalysis: Array<{ area: string; currentState: string; requiredState: string; gap: string }>;
  remediationPlan: Array<{ task: string; owner: string; dueDays: number; priority: 'P0' | 'P1' | 'P2' | 'P3' }>;
  riskRatingIfIgnored: 'LOW' | 'MEDIUM' | 'HIGH' | 'CATASTROPHIC';
  autoGeneratedAt: string;
}

